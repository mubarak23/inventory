import { Schema, model } from 'mongoose';

const tokenShema = Schema({
  userId: {
    type: Schema.Types.ObjectId,
    required: true,
    ref: 'User',
  },
  token: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    required: true,
  },
  updatedAt: {
    type: Date,
    required: true,
  },
});

const TokenModel = model('Token', tokenShema);

export default TokenModel;
